package com.nb.entity;

public class CodeMaster {
	
	private int codeMasterId;
	private String codeMasterSyntex;
	private String codeMasterFieldName;
	private String codeMasterFieldValue;
	public int getCodeMasterId() {
		return codeMasterId;
	}
	public void setCodeMasterId(int codeMasterId) {
		this.codeMasterId = codeMasterId;
	}
	public String getCodeMasterSyntex() {
		return codeMasterSyntex;
	}
	public void setCodeMasterSyntex(String codeMasterSyntex) {
		this.codeMasterSyntex = codeMasterSyntex;
	}
	public String getCodeMasterFieldName() {
		return codeMasterFieldName;
	}
	public void setCodeMasterFieldName(String codeMasterFieldName) {
		this.codeMasterFieldName = codeMasterFieldName;
	}
	public String getCodeMasterFieldValue() {
		return codeMasterFieldValue;
	}
	public void setCodeMasterFieldValue(String codeMasterFieldValue) {
		this.codeMasterFieldValue = codeMasterFieldValue;
	}
	

}
